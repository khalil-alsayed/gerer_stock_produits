    private void payerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payerActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) panier.getModel();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int total = Integer.parseInt(prixTotal.getText());
        if(total<1){
            JOptionPane.showMessageDialog(null, "Please select products first");
            return;
        }
        for (int i = 0; i < dtm.getRowCount(); i++) {
            Produit p = new Produit();
            p.setID(dtm.getValueAt(i, 0).toString());
            p.setNom(dtm.getValueAt(i, 1).toString());
            p.setQuantite(Integer.parseInt( dtm.getValueAt(i, 2).toString()));
            for (int j = 0; j < model.getRowCount(); j++) {
                String id = (String) model.getValueAt(j, 0);
                if(p.getID()==id){
                    int quantite = Integer.parseInt( model.getValueAt(j, 2).toString());
                    if(quantite < p.getQuantite() || p.getQuantite()<1){
                        JOptionPane.showMessageDialog(null, "quantite must be bigger than 0 and less than "+quantite);
                        return;
                    }
                    model.setValueAt(quantite-p.getQuantite(), j, 2);
                    model.fireTableCellUpdated(j, 2);
                }
            }
            gpro.updateProduit(p);
            p.setQuantite(Integer.parseInt( dtm.getValueAt(i, 2).toString()));
            System.out.println("before: "+dtm.getValueAt(i, 2).toString());
        gpro.insertRecord(p);
        }

        dtm.setRowCount(0);
        prixTotal.setText("0");
    }//GEN-LAST:event_payerActionPerformed

